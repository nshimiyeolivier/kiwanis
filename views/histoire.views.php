                  <p> En visitant ce site, vous souhaitez sans doute connaître l'histoire de la naissance de notre club que nous avons intitulé "Club Kiwanis Mons-Borinage" car plusieurs personnalités boraines partageant notre idéal avaient rejoint nos rangs à l'époque de sa fondation. </p>

                  <p>L'idée de fonder un club Kiwanis à Mons est venue tout naturellement d'un club de Charleroi. C'était en 1972.
                    A cette époque, le Kiwanis International créé en 1915 aux USA et implanté depuis une dizaine d'années seulement en Europe (1963), cherchait à parfaire son expansion en Belgique. Quelques clubs belges étaient déjà en place : Bruxelles, Liège, Charleroi…et un club était en formation à Tournai. </p>

                  <p>Dans la perspective donc d'établir un nouveau club dans la capitale du Hainaut, les kiwaniens carolos approchent et instruisent quelques montois de leurs relations. Ceux-ci, grâce aux attrayants préceptes et aux magnifiques objectifs philanthropiques du Kiwanis, sont aisément convaincus. Ils manifestent avec enthousiasme leur intention de fonder ce nouveau club. C'était le 2 mai 1972. </p>

                  <p>Dès 1973, notre club, à peine organisé, c'est-à-dire officiellement reconnu par les instances supérieures internationales du mouvement, parvient, grâce à ses 15 membres bénévoles, à faire un premier don à une œuvre locale pour la protection de la jeunesse. </p>

                  <p> En 1974, le Kiwanis club de Mons-Borinage reçoit solennellement sa Charte des mains du Gouverneur du District Benelux-France-Monaco du Kiwanis, en présence du Gouverneur de la Province, du Bourgmestre de Mons et des représentants de nombreux clubs ou associations caritatives de la région. La ville de Mons accueille ainsi en son sein un nouveau service-club qui, à côté des autres mouvements de la Ville, va œuvrer pour apporter sa pierre à l'édifice de la solidarité c'est-à-dire au bien-être des plus défavorisés notamment des jeunes. </p>

                  <p> Au cours de son histoire, le club Kiwanis Mons-Borinage a prouvé son dynamisme. Il s'est parfaitement intégré dans les structures nationales et internationales du mouvement. Ses membres se sont beaucoup investis dans l'organisation de nombreuses activités culturelles et autres en vue de recueillir les fonds nécessaires à la réalisation de l'un de leurs objectifs majeurs : Venir en aide aux enfants moins favorisés. </p>

